package de.hska.lat.robot.component.sensor.adjdS311;

public class AdjdS311ChannelSetings
{

	
	protected int capacitators;
	protected int integrationTime;
	protected int offset;
	
public AdjdS311ChannelSetings()
{
	
}	



public int getCapacitators()
{
	return capacitators;
}



public void setCapacitators(int capacitators)
{
	this.capacitators = capacitators;
}



public int getIntegrationTime()
{
	return integrationTime;
}



public void setIntegrationTime(int integrationTime)
{
	this.integrationTime = integrationTime;
}



public int getOffset()
{
	return offset;
}



public void setOffset(int offset)
{
	this.offset = offset;
}


	

	
}
